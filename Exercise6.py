def factorial(number):
    b = 1
    for i in range(1,number+1):
        b=b*i 
    return b  
print('The vactorial of number 5 is:',factorial(5))   # answer = 120
print('The vactorial of number 7 is:',factorial(7))   # answer = 5040 
print('The vactorial of number 10 is:',factorial(10))  # answer = 3628800
